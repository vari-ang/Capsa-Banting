    public void refreshRoom() {
        try {
            // TODO add your handling code here:
            DefaultTableModel model = (DefaultTableModel) jTableRoom.getModel();
        
            // Reset table
            model.setRowCount(0);
            
            out.writeBytes("REFRESH\n");
            
            // get results
            String resultsString = in.readLine();
            String[] resultsArr = resultsString.split(";");
            for (String arr : resultsArr) {
                String[] results = arr.split(":");
                
                // temporary object
                Object rowData[] = new Object[4];
                
                for (int i = 0; i < results.length; i++) {
                    rowData[i] = results[i];
                }
                
                // show the results to jTable
                model.addRow(rowData);
            }
        } catch (IOException ex) {
            Logger.getLogger(FormRoom.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
