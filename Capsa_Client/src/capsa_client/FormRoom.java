    private void jButtonJoin1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonJoin1ActionPerformed
        try {
            // TODO add your handling code here:
            DefaultTableModel model = (DefaultTableModel) jTableRoom.getModel(); 
            
            out.writeBytes("JOINROOM\n");

            // get selected ID value --> selected row on column 0
            String idRoom = model.getValueAt(jTableRoom.getSelectedRow(), 0).toString();
            String roomName = model.getValueAt(jTableRoom.getSelectedRow(), 1).toString();

            out.writeBytes(idRoom + "\n"); // send ID Room that the user wants to join
            out.writeBytes(username + "\n");

            String respond = in.readLine();
            JOptionPane.showMessageDialog(this, respond);
            if(respond.equalsIgnoreCase("Anda telah berhasil bergabung")) {
                // SHOW FormGame
                FormGame f = new FormGame(username, Integer.parseInt(idRoom), roomName);
                f.setVisible(true);
            }
        } catch (IOException ex) {
            Logger.getLogger(FormRoom.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonJoin1ActionPerformed

